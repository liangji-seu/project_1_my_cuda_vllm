# Header flags (matches C++ ModelConfigFlags)
FLAG_HAS_QKV_BIAS = 1 << 0
FLAG_HAS_QK_NORM  = 1 << 1
FLAG_HAS_O_BIAS   = 1 << 2
FLAG_HAS_MLP_BIAS = 1 << 3
FLAG_TIED_WEIGHTS = 1 << 4  # if set, lm_head shares embedding weight


def encode_flags(flags_dict: dict, config_dict: dict) -> int:
    """Encode flags into a single int32 bitfield."""
    val = 0
    if flags_dict.get("has_qkv_bias", False):
        val |= FLAG_HAS_QKV_BIAS
    if flags_dict.get("has_qk_norm", False):
        val |= FLAG_HAS_QK_NORM
    if flags_dict.get("has_o_bias", False):
        val |= FLAG_HAS_O_BIAS
    if flags_dict.get("has_mlp_bias", False):
        val |= FLAG_HAS_MLP_BIAS
    if config_dict.get("tie_word_embeddings", True):
        val |= FLAG_TIED_WEIGHTS
    return val


def serialize_fp32(fileobj, tensor: torch.Tensor):
    """Write one fp32 tensor to file."""
    d = tensor.detach().cpu().to(torch.float32).numpy().ravel()
    fileobj.write(struct.pack(f"{len(d)}f", *d))


def compute_rope_cache(head_size: int, max_seq_len: int, theta: float):
    """
    Compute RoPE cos/sin cache, LLaMA-style (half-rotate convention).

    Matches precompute_freqs_cis in model_qwen3.py:
      inv_freq = 1.0 / (theta ** (arange(0, dim, 2)[:dim//2] / dim))
      emb = cat((freqs, freqs), dim=-1)  # [seq_len, head_dim]
    """
    cos_cache = torch.zeros(max_seq_len, head_size, dtype=torch.float32)
    sin_cache = torch.zeros(max_seq_len, head_size, dtype=torch.float32)

    for pos in range(max_seq_len):
        for i in range(head_size):
            pair_idx = i % (head_size // 2)
            freq = 1.0 / (theta ** (2.0 * pair_idx / head_size))
            val = pos * freq
            cos_cache[pos, i] = math.cos(val)
            sin_cache[pos, i] = math.sin(val)

    return cos_cache, sin_cache


def export_bin(structure: dict, weights: dict, output_path: str):
    """Write .bin file deterministically from structure + weights."""
    config = structure["config"]
    flags = structure["flags"]
    export_order = structure["export_order"]

    head_size = config["head_dim"]
    max_seq_len = config["max_position_embeddings"]
    rope_theta = config["rope_theta"]
    vocab_size = config["vocab_size"]
    tied_embeddings = config["tie_word_embeddings"]

    # Legacy format: positive vocab_size = tied, negative = separate
    legacy_vocab_size = vocab_size if tied_embeddings else -vocab_size

    with open(output_path, "wb") as f:
        # Header: 8 x int32 = 32 bytes
        header = struct.pack(
            "iiiiiiii",
            config["hidden_size"],           # dim
            config["intermediate_size"],     # hidden_dim
            config["num_layers"],            # layer_num
            config["num_attention_heads"],   # head_num
            config["num_key_value_heads"],   # kv_head_num
            legacy_vocab_size,               # vocab_size (negative = untied)
            max_seq_len,                     # seq_len
            head_size,                       # head_dim
        )
        f.write(header)

        # Flags: 1 x int32 = 4 bytes (NEW — eliminates all C++ heuristics)
        flags_val = encode_flags(flags, config)
        f.write(struct.pack("i", flags_val))
        print(f"  Flags: 0x{flags_val:08x} (qkv_bias={flags['has_qkv_bias']}, "
              f"qk_norm={flags['has_qk_norm']}, tied={tied_embeddings})")

        # Weights in EXACT export_order sequence
        rope_cache = None
        tensor_count = 0
        for name in export_order:
            if name == "__rope_cos__":
                if rope_cache is None:
                    cos, sin = compute_rope_cache(head_size, max_seq_len, rope_theta)
                    rope_cache = (cos, sin)
                serialize_fp32(f, rope_cache[0])
                tensor_count += 1
            elif name == "__rope_sin__":
                serialize_fp32(f, rope_cache[1])
                tensor_count += 1
            else:
                tensor = weights[name]
                serialize_fp32(f, tensor)
                tensor_count += 1

            if tensor_count % 50 == 0:
                print(f"  Written {tensor_count}/{len(export_order)} entries...", end="\r")

        print(f"  Written {tensor_count}/{len(export_order)} entries. Done.")

    file_size = os.path.getsize(output_path)
    print(f"Output: {output_path} ({file_size / (1024**3):.2f} GB)")


# ============================================================
# Main
# ============================================================

def main():
    import argparse
    parser = argparse.ArgumentParser(
        description="Export .bin from model structure txt — ZERO guessing"
    )
    parser.add_argument("model_path", help="Path to HF model directory")
    parser.add_argument("--structure", "-s", default=None,
                        help="Path to model_structure.txt (default: <model_path>/model_structure.txt)")
    parser.add_argument("--output", "-o", required=True,
                        help="Output .bin file path")
    parser.add_argument("--dtype", default="fp32",
                        help="Export dtype (fp32 only for now)")
    args = parser.parse_args()

    model_path = args.model_path
    structure_path = args.structure or os.path.join(model_path, "model_structure.txt")

    # Step 1: Parse structure
    if not os.path.isfile(structure_path):
        print(f"ERROR: Structure file not found: {structure_path}")
        print("Run parse_model_structure.py first:")
        print(f"  python3 tools/parse_model_structure.py {model_path}")
        sys.exit(1)

    print(f"Reading structure: {structure_path}")
    structure = parse_structure_txt(structure_path)

    print(f"  Model: {structure['config']['model_type']}")
    print(f"  Layers: {structure['config']['num_layers']}, "
          f"Hidden: {structure['config']['hidden_size']}")
    print(f"  Flags: {dict(structure['flags'])}")

    # Step 2: Load weights
    print(f"\nLoading weights from: {model_path}")
    tensor_names = [n for n in structure["export_order"]
                    if n not in ("__rope_cos__", "__rope_sin__")]

    print(f"  Need {len(tensor_names)} tensors")
    weights = load_safetensors_weights(model_path, tensor_names)

    # Verify shapes
    for name in tensor_names:
        expected_shape = structure["tensor_shapes"].get(name)
        if expected_shape is not None:
            actual_shape = list(weights[name].shape)
            if actual_shape != expected_shape:
                raise ValueError(
                    f"Shape mismatch for {name}: "
                    f"expected {expected_shape}, got {actual_shape}"
                )
    print(f"  All {len(weights)} tensors loaded and verified")

    # Step 3: Export
    print(f"\nExporting .bin to: {args.output}")
    export_bin(structure, weights, args.output)


if __name__ == "__main__":
    main()
