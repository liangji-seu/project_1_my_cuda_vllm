#!/usr/bin/env python3
"""
Export .bin from model structure txt — ZERO guessing.

Reads the structure txt (generated by parse_model_structure.py), loads actual
weight values from safetensors, and writes the .bin file with deterministic layout.

Usage:
    # Step 1: Parse the model structure
    python3 tools/parse_model_structure.py /path/to/hf_model

    # Step 2: Export .bin
    python3 tools/export_from_structure.py /path/to/hf_model --output model.bin
"""

import json
import math
import os
import struct
import sys
from collections import OrderedDict
from pathlib import Path

import numpy as np
import torch


# ============================================================
# Structure txt parser
# ============================================================

def parse_structure_txt(txt_path: str) -> dict:
    """Parse model_structure.txt into a dict. No guessing — pure parsing."""
    config = OrderedDict()
    flags = OrderedDict()
    export_order = []
    tensor_shapes = OrderedDict()

    current_section = None

    with open(txt_path) as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue

            if line.startswith("[") and line.endswith("]"):
                current_section = line[1:-1]
                continue

            if current_section == "config":
                key, val = parse_kv(line)
                config[key] = val
            elif current_section == "flags":
                key, val = parse_kv(line)
                flags[key] = bool(val)  # parse_kv already returns correct type
            elif current_section == "export_order":
                # format: "tensor_name | [shape]" or "# __rope_cos__ ..."
                if " | " in line:
                    name, shape_str = line.split(" | ", 1)
                    export_order.append(name)
                    # Parse shape for verification
                    shape = eval(shape_str)  # safe: auto-generated
                    tensor_shapes[name] = shape
                elif line.startswith("# __rope"):
                    # Special entries
                    name = line.split()[1]
                    export_order.append(name)

    structure = OrderedDict({
        "config": config,
        "flags": flags,
        "export_order": export_order,
        "tensor_shapes": tensor_shapes,
    })

    return structure


def parse_kv(line: str):
    """Parse 'key = value' line. Value can be int, float, bool, or string."""
    key, val = line.split("=", 1)
    key = key.strip()
    val = val.strip()
    # Try typed parsing
    if val in ("true", "false"):
        return key, val == "true"
    try:
        return key, int(val)
    except ValueError:
        pass
    try:
        return key, float(val)
    except ValueError:
        pass
    return key, val


# ============================================================
# Weight loading
# ============================================================

def load_safetensors_weights(model_path: str, tensor_names: list) -> dict:
    """Load ONLY the needed tensors from safetensors, in fp32."""
    safetensors_path = os.path.join(model_path, "model.safetensors")

    if not os.path.isfile(safetensors_path):
        # Try sharded model
        index_path = os.path.join(model_path, "model.safetensors.index.json")
        if os.path.isfile(index_path):
            return _load_sharded_weights(model_path, tensor_names)
        raise FileNotFoundError(f"No safetensors at {model_path}")

    return _load_single_safetensors(safetensors_path, tensor_names)


def _load_single_safetensors(path: str, tensor_names: list) -> dict:
    """Load tensors from a single safetensors file."""
    with open(path, "rb") as f:
        header_size = struct.unpack("<Q", f.read(8))[0]
        header = json.loads(f.read(header_size))

    weights = {}
    with open(path, "rb") as f:
        data_start = 8 + header_size
        for name in tensor_names:
            if name not in header:
                raise KeyError(f"Tensor {name} not found in safetensors. Available: {sorted(header.keys())[:10]}...")
            meta = header[name]
            offset = meta["data_offsets"][0]
            dtype_str = meta["dtype"]
            shape = meta["shape"]

            f.seek(data_start + offset)
            raw = f.read(meta["data_offsets"][1] - meta["data_offsets"][0])

            tensor = _decode_tensor(raw, dtype_str, shape)
            weights[name] = tensor

    return weights


def _load_sharded_weights(model_path: str, tensor_names: list) -> dict:
    """Load tensors from sharded safetensors."""
    index_path = os.path.join(model_path, "model.safetensors.index.json")
    with open(index_path) as f:
        index = json.load(f)

    weight_map = index["weight_map"]
    shard_files = set(weight_map.values())

    # Group tensors by shard
    shard_to_tensors = {}
    for name in tensor_names:
        shard = weight_map.get(name)
        if shard is None:
            raise KeyError(f"Tensor {name} not found in weight map")
        shard_to_tensors.setdefault(shard, []).append(name)

    all_weights = {}
    for shard_file in sorted(shard_files):
        shard_path = os.path.join(model_path, shard_file)
        needed = shard_to_tensors.get(shard_file, [])
        if not needed:
            continue
        shard_weights = _load_single_safetensors(shard_path, needed)
        all_weights.update(shard_weights)

    return all_weights


def _decode_tensor(raw: bytes, dtype_str: str, shape: list):
    """Decode raw bytes to fp32 torch tensor."""
    dtype_map = {
        "F32": np.float32,
        "F16": np.float16,
        "BF16": np.uint16,  # bfloat16 needs special handling
        "F64": np.float64,
        "I32": np.int32,
        "I64": np.int64,
        "I8": np.int8,
        "U8": np.uint8,
        "BOOL": np.bool_,
    }
    np_dtype = dtype_map.get(dtype_str)
    if np_dtype is None:
        raise ValueError(f"Unknown dtype: {dtype_str}")

    arr = np.frombuffer(raw, dtype=np_dtype).reshape(shape)

    # Handle bfloat16: stored as uint16, interpret as bf16 → float32
    if dtype_str == "BF16":
        arr_uint16 = arr.astype(np.uint16)
        arr_fp32 = np.zeros(arr_uint16.shape, dtype=np.float32)
        # bf16: shift uint16 left by 16 to get float32 bits
        fp32_view = (arr_uint16.astype(np.uint32)) << 16
        arr_fp32 = fp32_view.view(np.float32)
        return torch.from_numpy(arr_fp32).float()

    return torch.from_numpy(arr.astype(np.float32)).float()


